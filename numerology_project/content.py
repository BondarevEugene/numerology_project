# Маппинг двузначных чисел дня рождения к однозначным базовым архетипам
def sync_archetypes():
    mapping = {
        "1": ["10", "19", "28"],
        "2": ["11", "20", "29"],
        "3": ["12", "21", "30"],
        "4": ["13", "22", "31"],
        "5": ["14", "23"],
        "6": ["15", "24"],
        "7": ["16", "25"],
        "8": ["17", "26"],
        "9": ["18", "27"]
    }
    for base, slaves in mapping.items():
        if base in ARCHETYPES:
            for s in slaves:
                ARCHETYPES[s] = ARCHETYPES[base].copy()
        if base in ARCHETYPE_EXTRAS:
            for s in slaves:
                ARCHETYPE_EXTRAS[s] = ARCHETYPE_EXTRAS[base].copy()

sync_archetypes()

